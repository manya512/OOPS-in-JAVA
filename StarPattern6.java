import java.util.*;
public class StarPattern6 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter number: ");
		int n = sc.nextInt();
		boolean m=i<=n;
		int i=1;
		while(m)
		{
			for(int j=1;j<=i;j++)
				System.out.print("* ");
			System.out.println();
			
			
		}
		sc.close();

	}

}
